#Se almacenan valores en variables enteras
var1 = 5
var2 = 3

#Se calcula la suma de los dos valores almacenados

print('\nResultado de la suma')
var3 = var1 + var2
print(var1,"+",var2,"=",var3)

#Se calcula la resta de los dos valores almacenados 

print('\n Resultado de la resta')
var3 = var1 - var2
print(var1,"-",var2,'=',var3)

#Se calcula la multipicación de los dos valores almacenados 

print('\n Resultado de la multiplicación')
var3 = var1 * var2
print(var1,"*",var2,'=',var3)

#Se calcula la división de los dos valores almacenados 
print('\n Resultado de la división')
var3 = var1 / var2
print(var1,"/",var2,'=',var3)

#Se calcula la división entera de los dos valores almacenados 
print('\n Resultado de la división entera')
var3 = var1 // var2
print(var1,"//",var2,'=',var3)

#Se calcula el módulo entre los dos valores almacenados
print('\n Resultado del módulo')
var3 = var1 % var2
print(var1,"%",var2,'=',var3)

#Se calcula la potencia entre los dos valores almacenados
print('\n Resultado del potencia')
var3 = var1 ** var2
print(var1,"**",var2,'=',var3)